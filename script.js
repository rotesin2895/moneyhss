function applyFilters() {
  var data = ALL_NEWS.slice();
  if (anState.currentCat !== "all") data = data.filter(function(n) { return n.cat === anState.currentCat; });
  if (anState.searchQuery) {
    var q = anState.searchQuery;
    data = data.filter(function(n) { return n.title.toLowerCase().indexOf(q) > -1 || n.desc.toLowerCase().indexOf(q) > -1 || n.catLabel.toLowerCase().indexOf(q) > -1; });
  }
  if (anState.sortBy === "newest") data.sort(function(a,b) { return b.dateSort - a.dateSort; });
  else if (anState.sortBy === "oldest") data.sort(function(a,b) { return a.dateSort - b.dateSort; });
  else if (anState.sortBy === "alpha") data.sort(function(a,b) { return a.title.localeCompare(b.title, "th"); });
  anState.filtered = data;
}

