function getFilteredVideos() {
  var list = VIDEO_DATA[vpState.currentTab].slice();
  if (vpState.searchQ) {
    var q = vpState.searchQ.toLowerCase();
    list = list.filter(function(v){ return v.title.toLowerCase().indexOf(q)>-1 || v.desc.toLowerCase().indexOf(q)>-1; });
  }
  if (vpState.sortBy==='popular') list.sort(function(a,b){ return parseInt(b.views.replace(/,/g,'')) - parseInt(a.views.replace(/,/g,'')); });
  else if (vpState.sortBy==='alpha') list.sort(function(a,b){ return a.title.localeCompare(b.title,'th'); });
  return list;
}

