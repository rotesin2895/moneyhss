/* Helpers */
function vcFmt(n) {
  n = parseInt(n) || 0;
  return n.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ',');
}
function vcParseViews(s) {
  return parseInt((s || '0').replace(/,/g, '')) || 0;
}

/* Storage */
var VC = {
  _KEY: 'hs4_vc_v1',
  _d: null,

  _load: function() {
    if (VC._d) return;
    try { VC._d = JSON.parse(localStorage.getItem(VC._KEY) || '{}'); } catch(e) { VC._d = {}; }
    if (!VC._d.news)  VC._d.news  = {};
    if (!VC._d.video) VC._d.video = {};
  },

  _save: function() {
    try { localStorage.setItem(VC._KEY, JSON.stringify(VC._d)); } catch(e) {}
  },

  get: function(type, id) {
    VC._load();
    return VC._d[type][String(id)] || 0;
  },

  increment: function(type, id) {
    VC._load();
    var k = String(id);
    VC._d[type][k] = (VC._d[type][k] || 0) + 1;
    VC._save();
    return VC._d[type][k];
  },

  totalType: function(type) {
    VC._load();
    var s = 0, d = VC._d[type] || {};
    Object.keys(d).forEach(function(k){ s += d[k]; });
    return s;
  },

  topId: function(type) {
    VC._load();
    var d = VC._d[type] || {}, top = null, max = 0;
    Object.keys(d).forEach(function(k){ if (d[k] > max){ max = d[k]; top = k; } });
    return top;
  }
};

/* Get display count for a video (base string count + session increments) */
function vcGetVideoDisplayCount(v) {
  var base = v._baseViews !== undefined ? v._baseViews : vcParseViews(v.views);
  if (v._baseViews === undefined) v._baseViews = base; // cache
  return base + VC.get('video', v.id);
}

/* Toast notification */
var _vcToastTimer = null;
function vcShowToast(type, title, count) {
  var wrap = document.getElementById('vcToastWrap');
  if (!wrap) return;
  // Remove old
  var old = wrap.querySelector('.vc-toast');
  if (old) { old.classList.remove('vc-in'); old.classList.add('vc-out'); }

  var t = document.createElement('div');
  t.className = 'vc-toast vc-in';
  var label  = type === 'video' ? 'วีดีโอ' : 'ข่าวสาร';
  var iconCls = type === 'video' ? 'fas fa-play-circle' : 'fas fa-newspaper';
  var shortTitle = title.length > 45 ? title.substring(0, 45) + '…' : title;
  t.innerHTML =
    '<div class="vc-toast-ico"><i class="' + iconCls + '"></i></div>' +
    '<div class="vc-toast-body">' +
      '<div class="vc-toast-type">' + label + '</div>' +
      '<div class="vc-toast-title">' + shortTitle + '</div>' +
    '</div>' +
    '<div class="vc-toast-pill"><i class="fas fa-eye"></i> ' + vcFmt(count) + ' ครั้ง</div>';

  wrap.appendChild(t);
  clearTimeout(_vcToastTimer);
  _vcToastTimer = setTimeout(function(){
    t.classList.remove('vc-in');
    t.classList.add('vc-out');
    setTimeout(function(){ if (t.parentNode) t.parentNode.removeChild(t); }, 320);
  }, 3500);
}

