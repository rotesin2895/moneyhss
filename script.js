function renderPagination(totalPages) {
  var wrap = document.getElementById("paginationWrap");
  if (!wrap) return;
  if (totalPages <= 1) { wrap.innerHTML = ""; return; }
  var p = anState.currentPage;
  var html = '<button class="page-btn prev-btn" ' + (p === 1 ? "disabled" : "") + ' onclick="goToPage(' + (p-1) + ')"><i class="fas fa-chevron-left"></i> ก่อนหน้า</button>';
  var pages = buildPageRange(p, totalPages);
  for (var i = 0; i < pages.length; i++) {
    if (pages[i] === "...") html += '<span class="page-dots">&#8230;</span>';
    else html += '<button class="page-btn ' + (pages[i] === p ? "active" : "") + '" onclick="goToPage(' + pages[i] + ')">' + pages[i] + '</button>';
  }
  html += '<button class="page-btn next-btn" ' + (p === totalPages ? "disabled" : "") + ' onclick="goToPage(' + (p+1) + ')">ถัดไป <i class="fas fa-chevron-right"></i></button>';
  wrap.innerHTML = html;
}

function buildPageRange(current, total) {
  var delta = 2, range = [], result = [], l;
  for (var i = 1; i <= total; i++) {
    if (i === 1 || i === total || (i >= current - delta && i <= current + delta)) range.push(i);
  }
  for (var j = 0; j < range.length; j++) {
    if (l) {
      if (range[j] - l === 2) result.push(l + 1);
      else if (range[j] - l > 2) result.push("...");
    }
    result.push(range[j]); l = range[j];
  }
  return result;
}

