function toggleFaq(btn) {
    var item = btn.parentElement;
    var answer = item.querySelector('.faq-answer');
    var isOpen = btn.classList.contains('open');
    var panel = item.closest('.faq-panel');
    panel.querySelectorAll('.faq-question.open').forEach(function(q) {
        q.classList.remove('open');
        q.parentElement.querySelector('.faq-answer').classList.remove('open');
    });
    if (!isOpen) {
        btn.classList.add('open');
        answer.classList.add('open');
    }
}

function switchFaqTab(tabKey, btnEl) {
    document.querySelectorAll('.faq-sidebar-btn').forEach(function(b) { b.classList.remove('active'); });
    if (btnEl) btnEl.classList.add('active');
    document.querySelectorAll('.faq-panel').forEach(function(p) { p.classList.remove('active'); });
    var target = document.getElementById('faqPanel-' + tabKey);
    if (target) {
        target.classList.add('active');
        target.querySelectorAll('.faq-question.open').forEach(function(q) {
            q.classList.remove('open');
            q.parentElement.querySelector('.faq-answer').classList.remove('open');
        });
    }
    var inp = document.getElementById('faqSearchInput');
    if (inp) inp.value = '';
    document.getElementById('faqNoResults').style.display = 'none';
}

function faqSearch(q) {
    q = (q || '').trim().toLowerCase();
    var noRes = document.getElementById('faqNoResults');
    if (!q) {
        noRes.style.display = 'none';
        document.querySelectorAll('.faq-item').forEach(function(item) { item.style.display = ''; });
        return;
    }
    var found = 0;
    document.querySelectorAll('.faq-panel').forEach(function(panel) {
        var panelHits = 0;
        panel.querySelectorAll('.faq-item').forEach(function(item) {
            var qText = item.querySelector('.faq-question') ? item.querySelector('.faq-question').textContent.toLowerCase() : '';
            var aText = item.querySelector('.faq-answer') ? item.querySelector('.faq-answer').textContent.toLowerCase() : '';
            if (qText.indexOf(q) > -1 || aText.indexOf(q) > -1) {
                item.style.display = '';
                panelHits++;
                found++;
            } else {
                item.style.display = 'none';
            }
        });
        if (panelHits > 0) {
            panel.classList.add('active');
            var panelId = panel.id.replace('faqPanel-', '');
            document.querySelectorAll('.faq-sidebar-btn').forEach(function(b) {
                b.classList.remove('active');
                if (b.getAttribute('onclick') && b.getAttribute('onclick').indexOf("'" + panelId + "'") > -1) {
                    b.classList.add('active');
                }
            });
        } else {
            panel.classList.remove('active');
        }
    });
    var activePanels = document.querySelectorAll('.faq-panel.active');
    activePanels.forEach(function(p, i) { if (i > 0) p.classList.remove('active'); });
    noRes.style.display = found === 0 ? 'block' : 'none';
    if (found === 0) {
        document.querySelectorAll('.faq-panel').forEach(function(p) { p.classList.remove('active'); });
    }
}