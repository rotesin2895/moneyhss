function injectImagesIntoBody(bodyHtml, images, altTitle) {
  // Split body by </p> or </h2></h2> to find injection points
  var sections = bodyHtml.split(/(<\/p>|<\/ul>)/);
  var result = [];
  var imgIdx = 0;
  var injectAt = []; // positions to inject images (after ~1st, ~middle, near end section)

  // Calculate injection positions: after 1st section, after middle section
  var total = sections.filter(function(s) { return s.match(/<\/p>|<\/ul>/); }).length;
  var step = Math.max(1, Math.floor(total / (images.length + 1)));
  var targets = [];
  for (var i = 0; i < images.length; i++) { targets.push(step * (i + 1)); }

  var closeCount = 0;
  for (var j = 0; j < sections.length; j++) {
    result.push(sections[j]);
    if (sections[j].match(/<\/p>|<\/ul>/)) {
      closeCount++;
      if (imgIdx < images.length && targets.indexOf(closeCount) !== -1) {
        var img = images[imgIdx];
        result.push(buildInlineImg(img.src || img, img.caption || (altTitle + " — ภาพที่ " + (imgIdx+1)), imgIdx, images.length));
        imgIdx++;
      }
    }
  }
  return result.join('');
}

